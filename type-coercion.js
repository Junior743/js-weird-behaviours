null + 3           // 3          -> null é convertido para 0
undefined + 3      // NaN        -> undefined vira NaN
'5' - 2            // 3          -> string vira número
'5' + 2            // '52'       -> concatenação
true + true        // 2          -> true é 1
false + 1          // 1          -> false é 0
'10' * '2'         // 20         -> strings viram números
'10' * 'a'         // NaN        -> string inválida vira NaN
